function generateValidationSchema(tool: ExtractedTool): string {
  const schemaName = `${tool.tool.name.replace(/_/g, '')}Schema`;
  const props: string[] = [];

  // Add path/query parameters
  for (const param of tool.parameters) {
    const zodType = getZodType(param.schema);
    const isOptional = !param.required;
    props.push(`  ${param.name}: ${zodType}${isOptional ? '.optional()' : ''}`);
  }

  // Add request body properties
  if (tool.requestBody?.content?.['application/json']?.schema?.properties) {
    const bodySchema = tool.requestBody.content['application/json'].schema;
    for (const [key, value] of Object.entries(bodySchema.properties as Record<string, any>)) {
      const zodType = getZodType(value);
      const isRequired = bodySchema.required?.includes(key);
      props.push(`  ${key}: ${zodType}${isRequired ? '' : '.optional()'}`);
    }
  }

  return `const ${schemaName} = z.object({
${props.join(',\n')}
});`;
}

function getZodType(schema: any): string {
  if (!schema) return 'z.unknown()';

  switch (schema.type) {
    case 'string':
      if (schema.format === 'uuid') return 'z.string().uuid()';
      if (schema.format === 'email') return 'z.string().email()';
      if (schema.enum) return `z.enum([${schema.enum.map((e: string) => `'${e}'`).join(', ')}])`;
      return 'z.string()';
    case 'number':
    case 'integer':
      return 'z.number()';
    case 'boolean':
      return 'z.boolean()';
    case 'array':
      return `z.array(${getZodType(schema.items)})`;
    case 'object':
      return 'z.record(z.unknown())';
    default:
      return 'z.unknown()';
  }
}

function generateToolCase(tool: ExtractedTool): string {
  // Parse the operationId to get router and method
  // e.g., "customers.list" -> router: "customers", method: "list"
  const [routerName, methodName] = tool.operationId.split('.');

  // Build the tRPC call based on the method
  if (tool.method === 'GET') {
    // For GET requests, call the query
    if (methodName === 'list') {
      return `    case '${tool.tool.name}': {
      const result = await trpcCaller.${routerName}.${methodName}(params);
      return { ${routerName}: result, count: Array.isArray(result) ? result.length : 1 };
    }`;
    } else {
      // get by id
      return `    case '${tool.tool.name}': {
      const result = await trpcCaller.${routerName}.${methodName}({ id: params.id as string });
      return result;
    }`;
    }
  } else if (tool.method === 'POST') {
    // For POST requests, call the mutation
    return `    case '${tool.tool.name}': {
      if (context.dryRun) {
        return { preview: true, wouldCreate: params };
      }
      const result = await trpcCaller.${routerName}.${methodName}(params);
      return result;
    }`;
  } else if (tool.method === 'PUT') {
    return `    case '${tool.tool.name}': {
      if (context.dryRun) {
        return { preview: true, wouldUpdate: { id: params.id, ...params } };
      }
      const result = await trpcCaller.${routerName}.${methodName}({ id: params.id as string, data: params });
      return result;
    }`;
  } else if (tool.method === 'DELETE') {
    return `    case '${tool.tool.name}': {
      if (context.dryRun) {
        return { preview: true, wouldDelete: params.id };
      }
      await trpcCaller.${routerName}.${methodName}({ id: params.id as string });
      return { deleted: true, id: params.id };
    }`;
  }

  return `    case '${tool.tool.name}': {
      throw new TRPCError({ code: 'NOT_IMPLEMENTED', message: 'Tool not yet implemented' });
    }`;
}

