function generateManifest(tools: ExtractedTool[], toolsContent: string): object {
  const hash = createHash('sha256').update(toolsContent).digest('hex').slice(0, 12);

  return {
    version: '1.0.0',
    generatedAt: new Date().toISOString(),
    contentHash: hash,
    toolCount: tools.filter((t) => t.tool.enabled !== false).length,
    tools: tools
      .filter((t) => t.tool.enabled !== false)
      .map((t) => ({
        name: t.tool.name,
        version: t.tool.version ?? 1,
        stability: t.tool.stability ?? 'stable',
        riskLevel: t.risk.level,
        scopes: t.tool.scopes ?? ['global'],
      })),
  };
}

