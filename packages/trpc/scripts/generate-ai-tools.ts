function extractToolsFromSpec(spec: OpenAPISpec): ExtractedTool[] {
  const tools: ExtractedTool[] = [];

  for (const [path, pathItem] of Object.entries(spec.paths)) {
    for (const [method, operation] of Object.entries(pathItem as Record<string, any>)) {
      // Skip if no AI extensions
      if (!operation['x-ai-tool']) continue;

      const tool: ExtractedTool = {
        operationId: operation.operationId,
        path,
        method: method.toUpperCase(),
        summary: operation.summary || '',
        tool: operation['x-ai-tool'],
        risk: operation['x-ai-risk'] || { level: 'LOW' },
        permissions: operation['x-ai-permissions'] || { required: [], minimumRole: 'viewer' },
        rateLimit: operation['x-ai-rate-limit'],
        cache: operation['x-ai-cache'],
        output: operation['x-ai-output'],
        parameters: operation.parameters || [],
        requestBody: operation.requestBody,
      };

      tools.push(tool);
    }
  }

  return tools;
}

