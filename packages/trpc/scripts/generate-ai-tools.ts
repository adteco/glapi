function buildParameterSchema(tool: ExtractedTool): any {
  const schema: any = {
    type: 'object',
    properties: {},
    required: [],
  };

  // Add path/query parameters
  for (const param of tool.parameters) {
    const propName = param.name;
    schema.properties[propName] = param.schema || { type: 'string' };
    if (param.description) {
      schema.properties[propName].description = param.description;
    }
    if (param.required) {
      schema.required.push(propName);
    }
  }

  // Add request body properties (for mutations)
  if (tool.requestBody?.content?.['application/json']?.schema) {
    const bodySchema = tool.requestBody.content['application/json'].schema;
    if (bodySchema.properties) {
      for (const [key, value] of Object.entries(bodySchema.properties)) {
        schema.properties[key] = value;
      }
      if (bodySchema.required) {
        schema.required.push(...bodySchema.required);
      }
    }
  }

  if (schema.required.length === 0) {
    delete schema.required;
  }

  return schema;
}

